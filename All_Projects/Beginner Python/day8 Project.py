def caesar(plain_text, shift_amount, way):
    end_text = ""
    if way == "decode":
        shift_amount *= -1
    for char in plain_text:
        if char in alphabet:
            place = alphabet.index(char)
            new_place = place + shift_amount
            new_text = alphabet[new_place]
            end_text += new_text
        else:
            end_text += char
